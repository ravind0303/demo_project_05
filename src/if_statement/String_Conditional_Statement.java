package if_statement;

public class String_Conditional_Statement {

    public static void main(String[] args) {

        String city="Jaipur";

      if (city=="Delhi"){
            System.out.println("Person belongs to Delhi");
      }

      if (city=="Jaipur") {

          System.out.println("Person belongs to Rajsthan");
      }

      if (city=="Aligarh"){

          System.out.println("Person belong to Uttar Pradesh");
      }

      if (city=="Pune"){

          System.out.println("Person belong to Maharashtra");
      }
    }
}
