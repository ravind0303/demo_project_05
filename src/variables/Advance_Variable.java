package variables;

public class Advance_Variable {

    public static void main(String[] args) {

        int a = 16;
        int b = 26;
       //Addision
        int sum = a + b;
        System.out.println("Variable value sum is : " +sum);


        int c=45;
        int d=25;
        //Subtract
        int sub=c-d;
        System.out.println("Variable value sub is : "+sub);

        int s=21;
        int p=5;
        //Multiple
        int multiple=s*p;

        System.out.println("Variable value multiple is : "+multiple);

        int z=55;
        int y=5;
        //Divide
        int divide=z/y;

        System.out.println("Variable value divide is : "+divide);

    }

}
