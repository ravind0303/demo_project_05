package for_loop;

public class For_Loop {
    public static void main(String[] args) {

        //Table of number

        for (int a=1; a<=10;a++){
            System.out.println("Print table of 2......." + a*2);
        }

        for (int a=10; a>=1;a--){
            System.out.println("Print reverse table of 2...... " +a*2);
        }

        for (int x=1;x<=10;x++){
            System.out.println(+x*3);
        }

        for (int y=10;y>=1;y--){

            System.out.println(+y*6);
        }

        }
    }





